export const monthNames = [
  'Січень',
  'Лютий',
  'Березень',
  'Квітень',
  'Травень',
  'Червень',
  'Липень',
  'Серпень',
  'Вересень',
  'Жовтень',
  'Листопад',
  'Грудень',
];

export const convertDate = (month: number, year: number): string => {
  return `${monthNames[month - 1]} ${year} рік`;
}
